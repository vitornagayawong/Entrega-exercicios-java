    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        // chamando o método verificarCredencias do UsuariosDao
        String email = txtEmail.getText();
        String senha = txtSenha.getText();

        UsuariosDao usuariosDao = new UsuariosDao();
        boolean credenciaisValidas = usuariosDao.verificarCredeciais(email, senha);

        if (credenciaisValidas) {
            TelaBoasVindas tbv = new TelaBoasVindas();
            tbv.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Email e/ou senha inválidos!");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

